package com.curso.ws.rs.bean;

/**
 *
 * @author Sandra
 */
public class Departamento {
    
    private int id;
    private String nombre;
    private int clave;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getClave() {
        return clave;
    }

    public void setClave(int clave) {
        this.clave = clave;
    }
    
    
    
}
